    void updateDisplay(String s) {
        if (displayField.getText().equals("0.0"))
            displayField.setText(s);
        displayField.setText(displayField.getText() + s);
    }
    
    void doMath() {
        if (!live) {
        numTwo = Double.parseDouble(displayField.getText());
        if (operator == '+')
            numOne += numTwo;
        if (operator == '-')
            numOne -= numTwo;
        if (operator == '/')
            numOne /= numTwo;
        if (operator == '*')
            numOne *= numTwo;
        }
    }
