    private void subtractActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subtractActionPerformed
        if(isFirst) {
            isFirst = false;
            numOne = Double.parseDouble(displayField.getText());
        } else {
            try {
                doMath();
            } catch (Exception e) {
                operator = '-';
                doMath();
            }
        }
        operator = '-';
        displayField.setText("" + numOne);
        live = true;
    }//GEN-LAST:event_subtractActionPerformed

